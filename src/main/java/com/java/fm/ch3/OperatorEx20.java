package com.java.fm.ch3;

public class OperatorEx20 {
    public static void main(String[] args) {
        System.out.println(-10%8);
        System.out.println(10%-8);
        System.out.println(-10%-8);

        // 나머지 연산자(%)는 나누는 수를 음수도 허용함.
        // 피연산자의 부호는 모두 무시하고 나머지 연산을 한 결과에 왼쪽 피연산자의 부호를 붙이면 됨.
    }
}
